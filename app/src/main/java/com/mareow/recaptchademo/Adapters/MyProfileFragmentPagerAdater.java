package com.mareow.recaptchademo.Adapters;

import androidx.annotation.NonNull;
import androidx.fragment.app.Fragment;
import androidx.fragment.app.FragmentManager;
import androidx.fragment.app.FragmentStatePagerAdapter;

import java.util.ArrayList;
import java.util.List;

public class MyProfileFragmentPagerAdater extends FragmentStatePagerAdapter {
    private final List<String> mFragmentTitleList = new ArrayList<>();
    private List<Fragment> mFragmentList;

    public MyProfileFragmentPagerAdater(@NonNull FragmentManager fm,List<Fragment> fragments) {
        super(fm);
    }

    @NonNull
    @Override
    public Fragment getItem(int position) {
        return mFragmentList.get(position);
    }

    @Override
    public int getCount() {
        return mFragmentList.size();
    }

    public void addFragment(Fragment fragment, String title) {
        mFragmentList.add(fragment);
        mFragmentTitleList.add(title);
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return mFragmentTitleList.get(position);
    }
}
